
# Estructura if...elif..else

a = 5
b = 7
c = 9

#Determinar cual número es el mayor.
#Es la misma estructura "Si" que vimos en pSeint
#if = si
#else = sino

# el operador '+' se utiliza para nir (concatenar) cadenas
# cuado usamos ',' no se necesita una onversión explicita
if a > b:
    print("El mayor es 'a': ", a)
elif b > c:
    print("El mayor es 'b': ",b)
else:
    print("El mayor es 'c': ",c)